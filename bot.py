# --- –û–±—Ä–∞–±–æ—Ç—á–∏–∫ /start ---
async def start_handler(update: Update, context: ContextTypes.DEFAULT_TYPE):
    user = update.effective_user
    name = escape(user.first_name)
    await update.message.reply_text(
        f"–ü—Ä–∏–≤–µ—Ç, [{name}](tg://user?id={user.id})! üëã\n"
        "–Ø –±–æ—Ç –¥–ª—è –≤–µ—Å—ë–ª—ã—Ö –¥–µ–π—Å—Ç–≤–∏–π. –ò—Å–ø–æ–ª—å–∑—É–π –∫–æ–º–∞–Ω–¥—ã –≤—Ä–æ–¥–µ:\n"
        "`/–æ–±–Ω—è—Ç—å`, `/–ø–æ—Ü–µ–ª–æ–≤–∞—Ç—å`, `/—É–¥–∞—Ä–∏—Ç—å`, `/—É–º–µ—Ä`, –∏ –¥—Ä—É–≥–∏–µ.",
        parse_mode="MarkdownV2"
    )


# --- –û–±—Ä–∞–±–æ—Ç–∫–∞ –≤—Å–µ—Ö —Ç–µ–∫—Å—Ç–æ–≤—ã—Ö —Å–æ–æ–±—â–µ–Ω–∏–π ---
async def message_handler(update: Update, context: ContextTypes.DEFAULT_TYPE):
    if not update.message or not update.message.text:
        return

    text = update.message.text.strip()
    command = text.split()[0]

    # –û—Ç–ø—Ä–∞–≤–∏—Ç–µ–ª—å ‚Äî –∫–ª–∏–∫–∞–±–µ–ª—å–Ω–æ–µ –∏–º—è
    sender_name = escape(update.effective_user.first_name)
    sender = f"[{sender_name}](tg://user?id={update.effective_user.id})"

    # --- –°–∞–º–æ-–¥–µ–π—Å—Ç–≤–∏—è ---
    if command in self_actions:
        action_text = self_actions[command]
        await update.message.reply_text(
            f"{sender} {action_text}",
            parse_mode="MarkdownV2"
        )
        return

    # --- –î–µ–π—Å—Ç–≤–∏—è —Å —Ü–µ–ª—å—é ---
    if command in actions:
        action_verb = actions[command]

        # 1. –ü—Ä–∏–æ—Ä–∏—Ç–µ—Ç ‚Äî @username
        mentioned_users = re.findall(r'@[\w\d_]+', text)
        if mentioned_users:
            target = mentioned_users[0]  # Telegram —Å–∞–º –¥–µ–ª–∞–µ—Ç —Å—Å—ã–ª–∫—É

        # 2. –û—Ç–≤–µ—Ç –Ω–∞ —Å–æ–æ–±—â–µ–Ω–∏–µ
        elif update.message.reply_to_message:
            target_user = update.message.reply_to_message.from_user
            target_name = escape(target_user.first_name)
            target = f"[{target_name}](tg://user?id={target_user.id})"

        # 3. –ò–º—è –ø–æ—Å–ª–µ –∫–æ–º–∞–Ω–¥—ã
        elif len(text.split()) > 1:
            target = escape(" ".join(text.split()[1:]))

        # 4. –ù–∏–∫–æ–≥–æ –Ω–µ —É–∫–∞–∑–∞–ª–∏
        else:
            target = "–≤—Å–µ—Ö ü´Ç"

        await update.message.reply_text(
            f"{sender} {action_verb} {target}",
            parse_mode="MarkdownV2"
        )


# --- –ó–∞–ø—É—Å–∫ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è ---
app = ApplicationBuilder().token(TOKEN).build()
app.add_handler(CommandHandler("start", start_handler))
app.add_handler(MessageHandler(filters.TEXT & ~filters.COMMAND, message_handler))

app.run_polling()
